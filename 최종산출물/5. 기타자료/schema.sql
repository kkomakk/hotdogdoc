ALTER TABLE metatag_weight_logs ADD CONSTRAINT fk_metatag_logs_tag FOREIGN KEY (metatag_id) REFERENCES meta_tags(id);
ALTER TABLE product_weight_logs ADD CONSTRAINT fk_product_logs_member FOREIGN KEY (member_id) REFERENCES members(id);
ALTER TABLE product_weight_logs ADD CONSTRAINT fk_product_logs_product FOREIGN KEY (product_id) REFERENCES products(id);

